                        <h4>Learning about Gold</h4>
                        <p class="justificado">
                        Gold is the king of materials used in jewelry, but why? It’s a natural element which keeps its properties changeless when exposed to air and water. It’s a malleable metal, a condition that makes it ideal for jewelry.
                        Gold is measured in karats (not carats as in diamonds) and is defined by how pure it is. So, pure gold is equal to 24 karats (24 k), this latter and 22 k are too soft for jewelry manufacturing, so both 18 k and 14 k are the most used. 18 k means 75% of pure gold and 14 k is 58,3 % gold, therefore the rest is a combination with other metals which give the required strength to be crafted into jewelry pieces. 
                        The content of every specific metal alloy is what gives the color of the gold. In its pure form, i.e. 24 k, gold is yellow but thanks to different combinations can be white, rose, blue, etc. 
                        </p>


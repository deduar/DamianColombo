                        <h3>Engagement Rings</h3><br>
                        <h4>Why diamonds are used for engagement rings?</h4>
                        <p class="justificado">
                        There are certain things that have become symbols, icons, for whatever reason. In the domain of engagements and marriage, a diamond ring is simply the most important icon. 
                        </p>
                        <p class="justificado">
                        Diamonds weren’t always considered a jewel. When they were first discovered thousands of years ago in India, they were used as decorations and as talismans to. Later they were used to “cure” diseases. It was only about a thousand years ago that diamonds were used as jewelry for the first time. A few centuries later, in 1477 the first man proposed with a diamond ring. This was Archduke Maximilian of Austria who asked Mary of Burgundy to be his wife. And there began a custom of proposing with diamond rings, although it was chiefly reserved for the upper class. While this was the first documented case of a diamond engagement ring, it is by no means the first engagement ring. That tradition has roots that date back to ancient Egypt when reed rings were placed on one another’s ring finger to symbolize their engagement. The ring finger was chosen because it was believed there was a vein in that finger that was connected directly to the heart, symbolizing love. 
                        </p>


                    <h4><center>Midiendo un anillo:</center></h4>


                        - Seleccione un anillo que utilice en el mismo dedo donde se pretende usar la pieza elegida.
                            <br>
                        - Los anillos de compromiso y las alianzas son tradicionalmente usadas en Argentina en el dedo anular de la mano izquierda.
                        <br>
                        - Coloque el anillo sobre los círculos que aparecen en la tabla de equivalencias.
                        <br>
                        - Cuando el contorno interior del anillo coincida con la línea negra del círculo, usted ha encontrado su talla.
                        <br>
                        - Para comprobar que la medida sea la correcta, le sugerimos que mida el diámetro interno del anillo con la línea que se encuentra debajo de cada talla.

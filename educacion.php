                    <h2>Diamond Certification</h2>
                    </p><p class="justificado">
                    What is a diamond certification done by an independent specialized laboratory?
</p><p class="justificado">
                    Although it’s not the only world renowned laboratory, the Gemological Institute of America (GIA), established in 1931, is the world-class authority on diamonds and colored stones. Its work includes research and education in the field of gemology.
</p>
                    <img src="assets/images/educacion/17.jpg"><BR>
                    <BR><BR>
                    <p class="justificado">

                    One of the GIA’s more significant services is provided by its GIA Laboratory which provides diamond grading services to describe the characteristics of loose diamonds. This information is issued by the GIA as the <b>GIA Diamond Grading Report</b>. 
                    </p><p class="justificado">
                    The <b>GIA Diamond Grading Report</b> (see copy below), issued for diamonds weighing 0.20 carats or more, provides a full quality assessment of a diamond and contains the following data:
                    </p><p class="justificado">

                    <ul class="list">
                    <li>  Date (of report)
                </li><li> Report Number
                </li><li> Laser Inscription Registry (if requested)
                </li><li> Shape and Cutting Style
                </li><li> Measurements
                </li><li> Color Grade 
                </li><li> Clarity Guide 
                </li><li> Finish (quality of surface condition as well as information of the facets)
                </li><li> Polish (general condition of surface)
                </li><li> Symmetry (exactness of diamond’s outline)
                </li><li> Fluorescence (strength and color of diamond)
                </li><li> Plotting Diagram (approximates shape and cutting style of stone)
                </li><li> Key to Symbols, GIA Clarity Scale, GIA Color Diamond Scale, Proportion Diagram, and Security Features (of report to ensure authenticity). As an optional service, a full color image of the diamond may also be included.</li>
                    </ul>

                    <img src="assets/images/educacion/18.jpg"><BR>
                    <BR><BR>

                    <h3>IGI</h3>
                    </p><p class="justificado">
                    Founded in Antwerp in 1975 IGI is the largest organization of its kind in the world with offices and gem labs in Antwerp, New York, Hong Kong, Mumbai, Bangkok, Tokyo, Dubai, Tel Aviv, Toronto, Los Angeles, Calcutta, New Delhi, Thrissur, Jaipur, Surat, Chennai, Ahmedabad and Hyderabad.
</p><p class="justificado">
                    It was the first lab in the world in sealing its diamonds for security reasons. In addition they patented the IGI Laserscribe System Mark which is used to inscribe identification data on the stone using a laser beam. Also it was one of the first labs able to identify treated natural diamonds and synthetic diamonds.
</p>
                    <img src="assets/images/educacion/19.jpg"><BR>
                    <BR><BR>

<p class="justificado">



                    <h3>EGL USA</h3>
</p><p class="justificado">
                    For over three decades EGL USA has portrayed state-of-the-art research lines in the gemological field in the USA with a distinguished balance between science and services. Its head office is located in the heart of the diamond district in New York and it has also an office in the west coast, in Los Angeles.
</p>
                    <img src="assets/images/educacion/20.jpg"><BR>

                        <h4>CLARITY</h4>

                        <p class="justificado">The definition of what diamond clarity is, describes the diamond's whole appearance in relation to the number of both internal and external imperfections in the stone. Each stone is assessed and then graded according to a standardized grading scale.
                        </p><p class="justificado">
                          Diamond clarity is divided into six categories, of which many contain a number of clarity grades. As the clarity grades move along the scale to a lower clarity grade, the overall value of the diamond decreases. Still, even though the clarity grade may drop, it might not necessarily affect the overall
                           appearance of the stone.
                        </p><p class="justificado">
                          The table below defines the different levels of clarity for both white and color diamonds.
                        </p>


                        <h3>4 C’s </h3>
                        <p class="justificado">Designated this way by its initials <b>Cut, Color, Clarity and Carat</b>.<p/>
                        <h4>CUT</h4>
                        <ul class="list">
                        <li>Cut is truly important,  since it has a great influence on both brightness and glow of the diamond.</li>

                        <li>Hence the importance of keeping the right proportions of the Cut, since this is crucial in unleashing the diamond true potential.</li>
                        </ul>
                        <img src="assets/images/educacion/01.jpg">

                        <p class="justificado">
                          As we observe on the above image, the light behavior by entering the stone shifts remarkably according to cut proportions.
                          A well cut diamond with symmetrical and straight facets will reflect light beautifully, i.e. light enters through the upper part or crown, then refracts on inner walls and goes back up through the crown, providing the diamond’s signature sparkle. On the contrary, when perfect proportions are not achieved, i.e. diamond is shallow or deep, light escapes through the pavillion.
                        </p><p class="justificado">
                        This is how you grade a diamond cut: Poor, Acceptable, Good, Very Good and Ideal. The keys to get to this classification are symmetry, polish and proportions.
                        </p><p class="justificado">
                        The four attributes of diamond quality, the 4 C’s, are all of utmost importance. However, the single attribute that man has the ability to perfect on a stone, is the diamond cut, task that must be performed only by experts.
                        </p>

                        <img src="assets/images/educacion/02.jpg"><BR>
                        Rough diamond cutting initial phase.
                        <br><br>
                        <p class="justificado">
                          The cut of the stone is connected to all the other attributes of quality. The cut will influence the diamond carat weight and the geometric proportions will affect the diamond color. A superb polishing can even help to get rid of external blemishes and therefore naturally enhance the diamond clarity.
                        </p><p class="justificado">

                        <p class="justificado"><b>The difference between the cut of a diamond and the making of a diamond.</b></p>
                        <p class="justificado">
                        In Nature there are no two rough diamonds identical. Performing a diamond cut is the ability of getting the best possible product from a  rough. By definition, the cut is the preset faceted arrangement of the diamond. Since it is through the cut that the finished product is made, how the faceted arrangement is created is referred to as the 'make' of the diamond.
                        </p>
                        <img src="assets/images/educacion/03.jpg"><BR>
                        Analysis of optical characteristics of a rough diamond.
                        <BR><BR>



